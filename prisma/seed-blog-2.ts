async function main() {
  let created = 0;
  for (const post of posts) {
    const exists = await prisma.post.findUnique({ where: { slug: post.slug } });
    if (!exists) {
      await prisma.post.create({ data: post });
      created++;
      console.log(`✓ [${post.category.padEnd(8)}] ${post.title}`);
    } else {
      console.log(`— skip: ${post.slug}`);
    }
  }
  console.log(`\nГотово. Добавлено: ${created} статей.`);
}

